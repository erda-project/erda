package pattern

import (
	"io"
)

const DeveloperScriptPattern = `"""
Generated by Erda Migrator.
Please implement the function entry, and add it to the list entries.
"""


import django.db.models

{{range $model := .Models}}
{{$model}}

{{end}}
def entry():
    """
    please implement this and add it to the list entries
    """
    pass


entries: [callable] = [
    entry,
]
`

type DeveloperScript struct {
	Models []string
}

func GenDeveloperScript(rw io.ReadWriter, migration DeveloperScript) error {
	return generate(rw, "DeveloperScriptPattern", DeveloperScriptPattern, migration)
}
